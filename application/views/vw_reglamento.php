	<h2>Reglamento de la quiniela por jornada</h2>
	<br>
	<h3>Consideraciones previas.</h3>
	<p>1. La quiniela se dividirá en:<br></p>
	<ul>
		<li>Quiniela por jornada.</li>
		<li>Quiniela en la liguilla (faltando dos jornadas para la liguilla se liberará el reglamento para esta quiniela).</li>
	</ul>
	<p>2. La quiniela por jornada permite inscribirte a cada una de las 17 quinielas (son las 17 jornadas del torneo), sin comprometerte a estar en todas.</p>
	<p>3. La quiniela por jornada tiene:<br></p>
	<ul>
		<li>Una bolsa de premios por jornada.</li>
		<li>Una bolsa de premios acumulada†.</li>
	</ul>
	<p>4.	La bolsa de premios por jornada se la lleva el participante que sume más puntos en toda la jornada.<br></p>
	<p>5.	La bolsa de premios acumulada se la lleva el participante que sume más puntos en toda la jornada.<br></p> 
	<p>6.	La bolsa de premios acumulada se repartirá dos veces.<br></p>
	<ul>
		<li>La primera bolsa acumulada se repartirá en la jornada 10.</li>
		<li>La segunda bolsa acumulada se repartirá en la jornada 17.</li>
	</ul>
	<p>†para poder llevarte la bolsa de premios acumulada:<br></p>
	<ul>
		<li>En las jornadas de la 1 a la 10 deberás haber participado al menos en 6 quinielas.</li>
		<li>En las jornadas de la 11 a la 17 deberás haber participado al menos en 4 quinielas.</li>
	</ul>
	<h3>Inscripción a la quiniela.</h3>
	<p>7. El costo de la inscripción para darse de alta como participante es de $30.00 pesos m.n.<br></p>
	<ul>
		<li>Este monto no será reembolsable una vez que hayan iniciado los partidos.</li>
	</ul>
	<p>8. Al cubrir el costo de la inscripción, el participante expresa su total acuerdo con el presente reglamento y se sujeta a sus condiciones.</p>
	<p>9. Una vez cubierto el costo de inscripción, la administración les pedirá su dirección de correo electrónico para utilizarlo como identificador y puedan publicar sus pronósticos.</p>
	<p>10.	La quiniela comprende todos los partidos de una jornada del Torneo Apertura 2014 de la Liga Bancomer MX.</p>
	<p>11. Para ser considerado válido, el pronóstico deberá publicarse a más tardar a las 10 a.m. del día en que empiezan los partidos de la jornada.</p>
	<p>12. Al final de cada jornada,  la administración integrará la información publicada en el blog quinielero.<br></p>
	<ul>
		<li>En base a los pronósticos válidos se calcularán los puntos generados por cada participante de acuerdo a las siguientes reglas:</li>
		<ul type="i">
			<li>Acertar el marcador final exacto suma 3 puntos.</li>
			<li>Acertar solamente a la tendencia* suma 1 punto.</li>
			<li>Cualquier otro caso suma 0 puntos.</li>
		</ul>
	</ul>
	<p>*Se entiende por acertar a la tendencia cuando el pronóstico acierta en el resultado (local gana o pierde o empata), pero no corresponde al marcador exacto.</p>
	<p></p>
	<h3>Distribución de los premios de la quiniela por jornada.</h3>
	<p>13. Asignación de las posiciones en la tabla general:</p>
	<ul>
		<li>La posición en la tabla general será definida por el número de puntos que haya sumado a lo largo de toda la jornada, ocupando el primer lugar el que más puntos haya sumado. En caso de empate en el criterio anterior, la posición en disputa se otorgará al participante que posea el mayor número de marcadores acertados.</li>
		<li>De persistir el empate, a los participantes involucrados se les asignará la misma posición.</li>
	</ul>
	<p>14.  Los criterios para distribuir la bolsa bolsa por jornada son los siguientes:</p>
	<ul type="i">
		<li>Si la primera posición acertó a todos los marcadores:
			<ul type="i"> 
				<li>El 90% del monto se otorga.</li>
				<li>El 10% restante será utilizado para cubrir gastos de administración de la quiniela.</li>
			</ul>
		</li>
		<li>Si la primera posición no acertó a todos los marcadores:
			<ul type="i"> 
				<li>80% del monto se otorga.</li>
				<li>10% del monto pasa a la bolsa acumulada.</li>
				<li>El 10% restante será utilizado para cubrir gastos de administración de la quiniela.</li>
			</ul>
		</li>
	</ul>
	<p>15.	Los criterios para distribuir la bolsa acumulada son los siguientes:</p>
	<ul>
		<li>La primera bolsa acumulada se repartirá en la jornada 10, el ganador será el que haya sumado más puntos en esa jornada, siempre y cuando haya participado en al menos 6 jornadas (comprende de la jornada 1 a la 10)  de lo contrario el ganador será el participante que siga en la tabla general y que cumpla con lo antes explicado.</li>
		<li>La segunda bolsa acumulada se repartirá en la jornada 17, el ganador será el que haya sumado más puntos en esa jornada, siempre y cuando haya participado en al menos 4 jornadas (comprende de la jornada 11 a la 17)  de lo contrario el ganador será el participante que siga en la tabla general y que cumpla con lo antes explicado.</li>
	</ul>
	<p>16.	Cualquier situación no considerada en el presente reglamento será resuelta por la administración de la quiniela sin posibilidad de apelación.</p>